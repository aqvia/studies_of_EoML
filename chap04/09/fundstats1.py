import numpy as np

x = np.array([
    29.1, 31.2, 30.9, 31.3, 26.6, 28.5, 31.5, 30.3,
    26.1, 30.7, 31.8, 28.4, 27.9, 27.1, 27.5, 28.5,
    29.3, 28.5, 32.0, 30.7, 28.2, 29.7, 25.3, 30.2,
    27.3, 26.2, 29.7, 32.8, 31.7, 30.8, 27.1
])

m1 = x.sum() / len(x)
m2 = x.mean()
s1 = np.sqrt(((x - m1)** 2).sum() / len(x))
s2 = x.std()
print("平均値:{:.4f}".format(m1))
print("平均値:{:.4f}".format(m2))
print("標準偏差:{:.4f}".format(s1))
print("標準偏差:{:.4f}".format(s2))