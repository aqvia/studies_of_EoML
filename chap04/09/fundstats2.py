import numpy as np


# 大阪:最高気温:2019-06-19 〜 2019-07-19
x = np.array([
    29.1, 31.2, 30.9, 31.3, 26.6, 28.5, 31.5, 30.3,
    26.1, 30.7, 31.8, 28.4, 27.9, 27.1, 27.5, 28.5,
    29.3, 28.5, 32.0, 30.7, 28.2, 29.7, 25.3, 30.2,
    27.3, 26.2, 29.7, 32.8, 31.7, 30.8, 27.1
])

# 札幌:最高気温:2019-06-19 〜 2019-07-19
y = np.array([
    25.5, 26.5, 20.1, 20.3, 24.6, 26.1, 26.1, 27.2,
    26.7, 21.5, 19.1, 22.3, 22.0, 23.1, 18.9, 24.2,
    23.4, 26.4, 24.8, 24.7, 27.0, 27.9, 24.5, 18.8,
    22.9, 24.8, 25.4, 24.9, 27.2, 26.5, 26.2
])

mx = x.mean()
my = y.mean()
sx = x.std()
sy = y.std()
sxy = ((x - mx) * (y - my)).sum() / len(x)
print("大阪:最高気温:標準偏差:{:4f}".format(sx))
print("札幌:最高気温:標準偏差:{:4f}".format(sy))
print("共分散:{:4f}".format(sxy))
print("相関係数:{:4f}".format(sxy / (sx * sy)))